						<p class="mgB-20">Tonight, the dining room opens with a menu that's not short of richness. Inspired by his time cooking at Husk in Nashville, though, McGlone says there's more to Southern cooking than heft. "I didn't want it to be super-heavy American fare; I wanted it to have some sort of refinement. That's what Husk was all about," he says. "They bring the high echelon of Southern food, but with a big hug, too. My approach here is similar - inviting but light."</p>
						<p class="mgB-20">So, for all the fried apple pie and St Louis pork ribs on the menu, there's also a focus on Southern-style greens and vegetables. McGlone has enlisted Noma Australia local forager Elijah Holland to propagate collard greens - a loose-leaf brassica similar to kale, and a staple of Southern US cuisine - for him in the Blue Mountains. He's particularly excited about the gourds, cooked over embers and served with brassicas and a mix of quinoa, wild rice and spelt grains. Cornmeal-crusted ling is similarly paired with dark leafy greens braised in dark beer and pork stock.</p>
